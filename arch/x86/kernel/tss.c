#include <rotary/tss.h>

/* ========================================================================= */

tss task_state;

/* ========================================================================= */

uint32 tss_init() {
    tss_flush();
    return 1;
}

/* ========================================================================= */

uint32 tss_set_esp0(void * esp0) {
    task_state.esp0 = (uint32)esp0;
    return 1;
}

/* ========================================================================= */

uint32 tss_set_ss0(uint32 ss0) {
    task_state.ss0 = ss0;
    return 1;
}

/* ========================================================================= */

tss * tss_get() {
    return &task_state;
}

/* ========================================================================= */
